        <p class="textNews2">PlayStation 5 consoles reach 7.8 million shipments worldwide, says Sony</p>
    </div>

    <div class="backColor3">
        The PlayStation 5 is a hit for Sony, since the firm has sold 7.8 million of its eagerly awaited PS5 games consoles worldwide, outpacing its predecessor, the PS4, after five months on the market. The results are highly encouraging, coming less than a year after the console’s launch.
        <br><br>
        Sony recently published its financial results for the fiscal year ending March 31, 2021. And the figures show that the PlayStation 5, launched last November, continues to prove a global hit. The firm shipped 4.5 million of its new consoles worldwide through the end of 2020, with the remaining 3.3 million PlayStation 5 shipments taking place between January through the end of March 2021. This uptake has even seen Sony outstrip the launch of its legendary PlayStation 4, over the same timeframe, with the PlayStation 4 selling 7.6 million units at the time.
        <br><br>
        <h3>Constantly growing demand</h3>
        <br><br>
        According to the NPD group, Sony’s PlayStation 5 is the fastest-selling games console in history in the United States, when looking at the first five months on the market. However, Nintendo has caught up, with the Switch moving back into the lead in terms of unit sales in the U.S. for the first quarter of 2021.
        <br><br>
        And this is somewhat logical, given the PlayStation 5 stock issues, with constantly increasing demand surpassing the number of available units. In spite of this, profit from Sony’s PlayStation business reached $3.14 billion for 2020, a new record for the firm.
